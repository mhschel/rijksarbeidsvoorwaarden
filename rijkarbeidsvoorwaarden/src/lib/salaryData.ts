// Get available schaal levels (1-19)
export function getAvailableSchalen(): number[] {
	return Array.from({ length: 19 }, (_, i) => i + 1);
}

// Get available tredes (always 0-10)
export function getAvailableTredes(): number[] {
	return Array.from({ length: 11 }, (_, i) => i);
}

// Get monthly compensation for a given schaal and trede
export function getMonthlyCompensation(schaal: number, trede: number): number {
	return salaryTable[schaal]?.[trede] || 0;
}
